create database foodfinder;
use foodfinder;


CREATE TABLE recipes (
    id INTEGER PRIMARY KEY AUTO_INCREMENT,
    recipe_name VARCHAR(250),
    recipe_one VARCHAR(250),
    recipe_two VARCHAR(250),
    recipe_three VARCHAR(250),
    image_one VARCHAR(250),
    image_two VARCHAR(250),
    image_three VARCHAR(250),
    title_one VARCHAR(250),
    title_two VARCHAR(250),
    title_three VARCHAR(250)

);
